<?php 

// Exibe mensagem de sucesso com classe CSS
function msgSuccess($msg){
    echo "<p class='success'> $msg </p>";
}

// Exibe mensagem de erro com classe CSS
function msgError($msg){
    echo "<p class= 'error'> $msg </p>";
}
// Exibe mensagem de aviso com classe CSS

function msgWarning($msg){
    echo "<p class = 'warning'> $msg </p>";
}
?>